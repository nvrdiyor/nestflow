const DICTS: Record<Lang, Dict> = { en, uz, ru };

export function t(key: string, params?: Record<string, string | number>): string {
  const lang = getLang();
  let s = DICTS[lang][key] ?? en[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
  }
  return s;
}

/** A small EN/UZ/RU switcher; wire clicks with wireLangSwitch. */
export function langSwitchMarkup(): string {
  const cur = getLang();
  return `<span class="lang-switch">${LANGS.map(
    (l) => `<button class="js-lang${l.code === cur ? ' active' : ''}" data-lang="${l.code}">${l.label}</button>`,
  ).join('')}</span>`;
}

export function wireLangSwitch(root: HTMLElement): void {
  root.querySelectorAll<HTMLButtonElement>('.js-lang').forEach((btn) => {
    btn.addEventListener('click', () => setLang(btn.dataset.lang as Lang));
  });
}
